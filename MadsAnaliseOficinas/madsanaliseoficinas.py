def listasVolumeVendas(top=10):
    if not compras:
        print("Nenhuma compra registada.")
        return

    df = pd.DataFrame(compras.values())

    # Agrupar por NIF da oficina
    resumo = df.groupby("NIF_Oficina").agg(
        Volume_Vendas=pd.NamedAgg(column="Valor", aggfunc="sum"),
        Qtde_Compras=pd.NamedAgg(column="ID", aggfunc="count")
    ).reset_index()

    # Arredondar volume de vendas para 2 casas decimais
    resumo["Volume_Vendas"] = resumo["Volume_Vendas"].round(2)

    # Adicionar nome da oficina
    resumo["Nome_Oficina"] = resumo["NIF_Oficina"].apply(lambda x: oficinas[x]["Nome"] if x in oficinas else "Desconhecido")

    # Ordenar pelo volume de vendas
    resumo = resumo.sort_values(by="Volume_Vendas", ascending=False).head(top)

    # Mostrar tabela organizada
    resumo = resumo[["NIF_Oficina", "Nome_Oficina", "Volume_Vendas", "Qtde_Compras"]]
    print(f"Top {top} oficinas por volume de vendas:")
    display(resumo.style.set_properties(**{'text-align': 'center'}).set_table_styles([{'selector': 'th','props': [('text-align', 'center')]}]))




def visualizarEvolucaoDespesas(nifUtilizador=None):
    if not compras:
        print("Nenhuma compra registada.")
        return

    df = pd.DataFrame(compras.values())
    df["Data"] = pd.to_datetime(df["Data"])

    if nifUtilizador:
        df = df[df["NIF_Utilizador"] == nifUtilizador]

    if df.empty:
        print("Nenhuma compra encontrada para os filtros fornecidos.")
        return

    # Agrupa por data e soma valores
    df_grouped = df.groupby("Data")["Valor"].sum().reset_index()

    # Plot
    plt.figure(figsize=(10,5))
    plt.plot(df_grouped["Data"], df_grouped["Valor"], marker='o', linestyle='-')
    plt.title(f"Evolução das Despesas{' do Utilizador '+nifUtilizador if nifUtilizador else ''}")
    plt.xlabel("Data")
    plt.ylabel("Valor (€)")
    plt.grid(True)
    plt.xticks(rotation=45)
    plt.tight_layout()
    plt.show()

