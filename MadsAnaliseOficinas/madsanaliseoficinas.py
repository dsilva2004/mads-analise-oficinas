def registarCompra(nifUtilizador: str, nifOficina: str, valor: float, descricao: str, dataCompra: str, catCompra: str):

    erros = []

    # Verificar se todos os campos foram preenchidos
    if not all([nifUtilizador, nifOficina, valor, descricao]):
        erros.append("Erro: Todos os campos são obrigatórios.")

    # Validar NIF do utilizador
    if not isinstance(nifUtilizador, str):
        erros.append("Erro: O NIF do utilizador deve ser texto numérico.")
    elif not nifUtilizador.isdigit():
        erros.append("Erro: O NIF do utilizador deve conter apenas números.")
    elif len(nifUtilizador) != 9:
        erros.append("Erro: O NIF do utilizador deve ter 9 dígitos.")
    elif nifUtilizador not in utilizadores:
        erros.append("Erro: Não existe nenhum utilizador com esse NIF.")

    # Validar NIF da oficina
    if not isinstance(nifOficina, str):
        erros.append("Erro: O NIF da oficina deve ser texto numérico.")
    elif not nifOficina.isdigit():
        erros.append("Erro: O NIF da oficina deve conter apenas números.")
    elif len(nifOficina) != 9:
        erros.append("Erro: O NIF da oficina deve ter 9 dígitos.")
    elif nifOficina not in oficinas:
        erros.append("Erro: Não existe nenhuma oficina com esse NIF.")

    # Validar valor
    if not isinstance(valor, (int, float)):
        erros.append("Erro: O valor da compra deve ser numérico.")
    elif valor <= 0:
        erros.append("Erro: O valor da compra deve ser maior que zero.")

    # Validar descrição
    if not isinstance(descricao, str):
        erros.append("Erro: A descrição deve ser texto.")
    else:
        descricao = descricao.strip()
        if not descricao:
            erros.append("Erro: A descrição não pode estar vazia.")
        elif len(descricao) < 3:
            erros.append("Erro: A descrição deve ter pelo menos 3 caracteres.")

    # Validar data (apenas números e no formato: YYYY-MM-DD)
    data_convertida = None

    if not isinstance(dataCompra, str) or not re.match(r"^\d{4}-\d{2}-\d{2}$", dataCompra):
        erros.append("Erro: A data de compra deve estar no formato YYYY-MM-DD.")
    else:
        try:
            data_convertida = datetime.datetime.strptime(dataCompra, "%Y-%m-%d").date()
        except ValueError:
            erros.append("Erro: Data de compra inválida.")

    # Verificar se é futura
    if data_convertida:
        hoje = datetime.date.today()
        if data_convertida > hoje:
            erros.append("Erro: A data de compra não pode ser futura.")

    # Validar categoria de compra
    categoria_compra = ["P", "S"]
    if catCompra not in categoria_compra:
        erros.append("Erro: Categoria de compra inválido. Use 'P' para 'Produto' ou 'S' para 'Serviço'.")

    # Verificar se a compra já existe (todos os dados iguais)
    for compra in compras.values():
        if (compra["NIF_Utilizador"] == nifUtilizador and
            compra["NIF_Oficina"] == nifOficina and
            compra["Valor"] == valor and
            compra["Descricao"] == descricao and
            compra["Categoria_Compra"] == catCompra):
            erros.append("Erro: Já existe uma compra registada com os mesmos dados.")
            break

    # Mostrar erros
    if erros:
        print("Foram encontrados erros no comando:")
        for erro in erros:
            print(f"- {erro}")
        return

    # Criar ID da compra
    idCompra = len(compras) + 1

    # Registar compra
    compras[idCompra] = {
        "ID": idCompra,
        "NIF_Utilizador": nifUtilizador,
        "NIF_Oficina": nifOficina,
        "Valor": valor,
        "Descricao": descricao,
        "Data": data_convertida,
        "Categoria_Compra": catCompra,
    }

    print(f"Compra registada com sucesso! ID da compra: {idCompra}")

