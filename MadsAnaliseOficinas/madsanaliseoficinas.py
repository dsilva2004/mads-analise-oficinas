def exportarParaJSON():
    # Função auxiliar para converter datas em strings
    def converter(obj):
        if isinstance(obj, (date, dt)):
            return obj.isoformat()  # formato 'YYYY-MM-DD' ou 'YYYY-MM-DDTHH:MM:SS'
        return obj

    # Criar dicionário principal
    dados = {
        "utilizadores": utilizadores,
        "oficinas": oficinas,
        "compras": compras,
        "categoriasOficinas": categoriasOficinas
    }

    # Exportar para ficheiro JSON com tratamento de datas
    with open("dados_exportados.json", "w", encoding="utf-8") as f:
        json.dump(dados, f, ensure_ascii=False, indent=4, default=converter)

    print("Dados exportados com sucesso para 'dados_exportados.json'.")


